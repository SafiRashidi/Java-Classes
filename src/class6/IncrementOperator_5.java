package class6;

public class IncrementOperator_5 {
    public static void main(String[] args) {

        int num=10;
        num=num+1;
        System.out.println(num);
        num+=1;
        System.out.println(num);
        num++; // also increments the number by 1
        System.out.println(num);

        int num2=10;
        num2--; // decrements the value by one
        System.out.println(num2);

    }
}
