package class2;

public class DecimalNumber {
    public static void main(String[] args) {
        /*
        if we want to store decimal numbers (10.5 110.2 1.6) in computer's memory using java
        we can use the following types:
        1) float 2) double
        decimal
         */
        float myNumberBox=10.5f;
        float mySecondBox=18.6f;
        double largeDecimalBox=123456789.548797;
        System.out.println(myNumberBox);
        /*
        If we want to store characters (#,$,o,s,4) we can use char datatype
         */
        char letter='s';
        char gender='M';
        char symbol='#';
        char numberChart='1';

        String name="Safi";
        /*
        String data type belongs to a different category called non-primitives
        that's why for String rules are a little different for example S of
        String is upper case, the color is also not blue
         */
        boolean hungry=true;
        boolean sleepy=false;
    }
}
